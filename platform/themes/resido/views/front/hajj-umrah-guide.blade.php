            <p class=" mt-3" >This means moving around Kabah seven times with respect and devotion.The pilgrims must walk keeping Kabah on their left. Upon arriving at the Kaabah, stop reciting the Talbiyyah. You must be in Wudu (ablution). Men to leave their right shoulder bare ONLY during the entire Tawaf, this is called Idtiba’a, after the tawaf men need to cover both shoulders.
              <br>
              <ul class="list-style">
                <li>Start at the Black Stone, either</li>
                <li>Kiss it if you can, if not.  </li>
                <li>Touch it with your right hand, if not  </li>
                <li>Face the Black Stone and point at it with your right hand</li>
              </ul></p><br>

