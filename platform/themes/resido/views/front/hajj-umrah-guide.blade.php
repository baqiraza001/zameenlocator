            <h4 class="text-center mt-4" style="color:#2eca6a;font-size: 35px;"> <b>Day By Day Detail's of Hajj</b> </h4>

            <h4 class="text-center mt-5"> <b>1. 8th Zil Hajj</b> </h4>
            <p class="mt-3">8thZil Hajj starts after the Maghrib prayer of 7th Zil Hajj. The Pilgrims will make their preparations for Hajj same as they got prepared for Umrah. I.e. they will take bath/ Ghusal, set intention, wear Ihram and declare Talbiya.

              <span style="font-weight:700;">Departure to Mina from Azizia</span>

            On 8th Zil Hajj after Fajr prayers OR after the sunrise the Pilgrims will leave towards Mina from Azizia while reciting:</p>

            <p class="mt-3">اللّهُ أكبر اللّهُ أكبر, لا إلَهَ الا اللّه, اللّهُ أكبر اللّهُ اكبر, و لِلّه الحمدَ

            </p>
            <p class="mt-3">. Here they will offer remaining four Prayers (Zuhr, Asar, Maghrib and Isha) and will stay overnight in Mina.
            </p>






            <h4 class="text-center mt-4"> <b>2. 9th Zil Hajj</b> </h4>
            <p class="mt-3"><span style="font-weight:700;">Departure for Arafat from Mina</span><br>

              After Fajr prayers in Mina. The pilgrims will proceed to Arafat ground. Here they will offer Zuhrain (Zuhar+Asar).<br>

              <span style="font-weight:700;">Departure for Muzdalifa from Arafat</span><br>

              After Zuhrain in Arafat, the Pilgrims will move towards Muzdalifa after sunset (decling of Sunset), where they will offer Maghribain (combined prayers of Maghrib & Isha prayers) and stay overnight. During different intervals of night, the pilgrims will collect 49 very small size pebbles for next day.
            </p>





            <h4 class="text-center mt-4"> <b>3. 10th Zil Hajj</b> </h4>
            <p class="mt-3">On the first day of Eid, the pilgrims will leave for Mina from Muzdalifa after Fajr Prayers. At Mina they will stone Jamarat ul Kubra and will perform Qurbani. After the ritual of Qurbani male Pilgrims will shave their heads and female will clip their hair as part of the Hajj. Now the pilgrims are free to remove their Ihram and wear their ordinary dresses and can perform Tawaf al Ifada. Two (2) Rakat of Nawafil will be offered near Maqam e Ibrahim.They will drink zamzam after performing Sa’ey i.e. 7 runs between Safwah & Marwah and will stay overnight in Mina.</p>

            <h4 class="text-center mt-4"> <b>4. 11th Zil Hajj</b> </h4>
            <p class="mt-3">On second day of Eid, the pilgrims will proceed to Jamarat from Mina after Zuhr prayers. They will stone all the three Jamarats (Jamarat e Sughra, Jamarat e Wusta and Jamarat e Qubra) in sequence from small to large by throwing 7 pebbles each. After completion of this ritual, pilgrims will go back to Mina for night stay.
            </p>

            <h4 class="text-center mt-4"> <b>5. 12th Zil Hajj</b> </h4>
            <p class="mt-3">Last day of Eid will again start by proceeding towards Jamarats from Mina after Zuhar Prayers. 4th day stoning of Jamarat will be completed from small to large. After stoning Pilgrims will leave for Makkah before Sunset. Before leaving Makkah, the pilgrims will perform Tawaf ul Wadda (Goodbye Tawaf). At this stage all the obligatory rituals of Hajj are complete.</p></div>


