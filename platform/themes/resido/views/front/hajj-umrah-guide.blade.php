            <h2 style="color:#2eca6a;">بِسْمِ اللَّهِ, وَ الصلاة والسلام عَلَى رسول الله, اللَّهُمَّ إِنِّي أَسْأَلُكَ مِنْ فَضْلِكَ,
            </h2>
