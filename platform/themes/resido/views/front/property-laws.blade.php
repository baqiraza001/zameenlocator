             <h3><strong>Capital Value Tax (CVT)</strong></h3>
              <p>CVT is imposed on the capital value of immovable properties at the time of transfer.
             Rates can vary by province, and exemptions may apply for certain types of transactions.</p>
            
             <h4><strong>Stamp Duty</strong></h4>
              <p>Stamp duty is levied on various documents, including property sale deeds and lease agreements.
             The rates differ by province, and stamp duty must be paid at the time of property transfer.</p>

            <h4><strong>Property Tax</strong></h4>
              <p>Property tax is an annual tax imposed by local authorities on the annual rental value of the property.
             Rates can vary, and it's usually the responsibility of the owner to pay this tax.</p>

             <h4><strong>Withholding Tax</strong></h4>
              <p>Withholding tax is applicable on the sale of property, and the buyer is required to deduct and deposit this tax with the tax authorities.</p>
          
             <h4><strong>Income Tax on Rental Income</strong></h4>
              <p>Rental income is subject to income tax. The property owner is required to file an income tax return and declare rental income.</p>

             <h4><strong>Betterment Tax</strong></h4>
             <p>Some local authorities may impose betterment tax when there is an increase in property value due to development activities.</p>
            
            
            
